"""Compare the GhidraSharp and pyghidra parity dumps, write REPORT.md, and exit
non-zero on any mismatch (so this doubles as a CI gate).

    python bench/compare.py [out-dir]   # default: bench/out
"""
from __future__ import annotations

import hashlib
import json
import sys
from pathlib import Path

ARTIFACTS = ["functions", "symbols", "decompile", "instructions", "xrefs_to", "bytes", "function_detail"]


def sha(p: Path) -> str | None:
    return hashlib.sha256(p.read_bytes()).hexdigest() if p.exists() else None


def load(p: Path) -> dict:
    return json.loads(p.read_text()) if p.exists() else {}


def main(argv: list[str]) -> int:
    base = Path(argv[0]) if argv else Path("bench/out")
    cs, py = base / "cs", base / "py"
    cst, pyt = load(cs / "timings.json"), load(py / "timings.json")

    rows = []
    all_ok = True
    for a in ARTIFACTS:
        h_cs, h_py = sha(cs / f"{a}.txt"), sha(py / f"{a}.txt")
        identical = h_cs is not None and h_cs == h_py
        all_ok = all_ok and identical
        rows.append((a, identical, cst.get(a, {}), pyt.get(a, {}), h_cs))

    cs_total = sum(v.get("ms", 0) for v in cst.values())
    py_total = sum(v.get("ms", 0) for v in pyt.values())

    out = []
    out.append("# Parity report — GhidraSharp vs pyghidra\n")
    out.append("Both sides drive the **same Ghidra** with the same calls, so identical output is")
    out.append("the bar. This file is generated by `bench/compare.py`.\n")
    out.append(f"**Verdict: {'✅ identical across the whole API' if all_ok else '❌ MISMATCH'}**\n")

    out.append("## Correctness (output identical to pyghidra?)\n")
    out.append("| Capability | Records | Identical | SHA-256 |")
    out.append("| --- | ---: | :---: | --- |")
    for a, identical, c, _p, h in rows:
        mark = "✅" if identical else "❌"
        out.append(f"| {a} | {c.get('count', '?')} | {mark} | `{(h or '—')[:12]}` |")

    out.append("\n## Performance (wall time, ms)\n")
    out.append("| Capability | pyghidra | GhidraSharp | notes |")
    out.append("| --- | ---: | ---: | --- |")
    notes = {
        "functions": "1 call", "symbols": "1 call", "decompile": "1 batch call (streamed)",
        "instructions": "1 call/function", "xrefs_to": "1 call/function", "bytes": "1 call/function",
        "function_detail": "1 call/function",
    }
    for a, _i, c, p, _h in rows:
        out.append(f"| {a} | {p.get('ms', '?')} | {c.get('ms', '?')} | {notes.get(a, '')} |")
    out.append(f"| **total** | **{py_total}** | **{cs_total}** | |")

    out.append(
        "\nThe single-round-trip operations (functions, symbols, batched decompile) are the"
        "\napples-to-apples speed comparison; the per-function loops are chatty by construction"
        "\nhere (one RPC each) and exist to prove output parity, not to be called that way in bulk.\n")

    out.append("## Reproduce\n")
    out.append("```")
    out.append("pwsh bench/run.ps1 <path/to/PROJECT.gpr>")
    out.append("```")
    out.append("Needs `GHIDRA_INSTALL_DIR` + `JAVA_HOME`, and `pyghidra` installed for the baseline.\n")

    report = Path("bench/REPORT.md")
    report.write_text("\n".join(out), encoding="utf-8")
    print(f"{'OK' if all_ok else 'MISMATCH'} -> {report}  (cs {cs_total} ms / py {py_total} ms)")
    return 0 if all_ok else 1


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))
